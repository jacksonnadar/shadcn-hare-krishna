export const excelDateToJSDate = (serial: any) => {
  const utc_days = Math.floor(serial - 25569);
  const utc_value = utc_days * 86400;
  const date_info = new Date(utc_value * 1000);
  const fractional_day = serial - Math.floor(serial) + 0.0000001;
  let total_seconds = Math.floor(86400 * fractional_day);
  const seconds = total_seconds % 60;
  total_seconds -= seconds;
  const hours = Math.floor(total_seconds / (60 * 60));
  const minutes = Math.floor(total_seconds / 60) % 60;
  return new Date(
    date_info.getFullYear(),
    date_info.getMonth(),
    date_info.getDate(),
    hours,
    minutes,
    seconds
  );
};

export const unitToSeconds = (unit: string, value: number) => {
  switch (unit) {
    case 'seconds':
      return value;
    case 'minutes':
      return value * 60;
    case 'hours':
      return value * 3600;
    default:
      return value;
  }
};

export const secsTohhmmss = (secs: number) => {
  const hours = Math.floor(secs / 3600);
  const minutes = Math.floor((secs % 3600) / 60);
  const seconds = Math.floor((secs % 3600) % 60);

  return [hours, minutes, seconds].map((v) => (v < 10 ? '0' + v : v)).join(':');
};

export const hhmmssToSecs = (hhmmss: string) => {
  const [hours, minutes, seconds] = hhmmss
    .split(':')
    .map((v) => parseInt(v, 10));
  return hours * 3600 + minutes * 60 + seconds;
};

export const hhmmssSubtraction = (hhmmss1: string, hhmmss2: string) => {
  const secs1 = hhmmssToSecs(hhmmss1);
  const secs2 = hhmmssToSecs(hhmmss2);
  const diff = secs1 - secs2;

  //allway poseitive
  return diff < 0 ? '-' + secsTohhmmss(Math.abs(diff)) : secsTohhmmss(diff);
};

export const hhmmssAddition = (hhmmss1: string, hhmmss2: string) => {
  const secs1 = hhmmssToSecs(hhmmss1);
  const secs2 = hhmmssToSecs(hhmmss2);
  return secsTohhmmss(secs1 + secs2);
};

